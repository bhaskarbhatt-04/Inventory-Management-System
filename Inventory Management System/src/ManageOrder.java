    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String noOfUnits = jTextField7.getText();
        if (!noOfUnits.equals("")) {
            String productName = jTextField4.getText();
            String productDescription = jTextField6.getText();
            String productPrice = jTextField5.getText();

            int totalPrice = Integer.parseInt(jTextField7.getText()) * Integer.parseInt(productPrice);

            int checkStock = 0;
            int checkProductAlreadyExistInCart = 0;

            try {
                Connection conn = ConnectorProvider.getCon();
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery("select * from product where product_pk=" + productPk + "");
                while (rs.next()) {
                    if (rs.getInt("quantity") >= Integer.parseInt(noOfUnits)) {
                        checkStock = 1;
                    } else {
                        JOptionPane.showMessageDialog(null, "Sorry!, the product is out of stock. Only" + rs.getInt("quantity") + "left");
                    }

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

            if (checkStock == 1) {
                DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
                if (jTable3.getRowCount() != 0) {
                    for (int i = 0; i < jTable3.getRowCount(); i++) {
                        if (Integer.parseInt(model.getValueAt(i, 0).toString()) == productPk) {
                            checkProductAlreadyExistInCart = 1;
                            JOptionPane.showMessageDialog(null, "Product already exist in cart");
                        }
                    }
                }

                if (checkProductAlreadyExistInCart == 0) {
                    model.addRow(new Object[]{productPk, productName, noOfUnits, productPrice, productDescription, totalPrice});
                    finalTotalPrice = finalTotalPrice + totalPrice;
                    jLabel16.setText(String.valueOf(finalTotalPrice));
                    JOptionPane.showMessageDialog(null, "Product added in cart");

                }
                clearProductFields();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Order quantity is required");
        }

    }//GEN-LAST:event_jButton5ActionPerformed

