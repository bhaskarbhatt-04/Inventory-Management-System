    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        int index = jTable3.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to remove this product", "Select", JOptionPane.YES_NO_OPTION);

        if (a == 0) {
            TableModel model = jTable3.getModel();
            String subTotal = model.getValueAt(index, 5).toString();
            finalTotalPrice = finalTotalPrice - Integer.parseInt(subTotal);
            jLabel16.setText(String.valueOf(finalTotalPrice));
            ((DefaultTableModel) jTable3.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_jTable3MouseClicked

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (finalTotalPrice != 0 && !jTextField1.getText().equals("")) {
            String orderId = getUniqueId("Bill-");

            DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
            if (jTable3.getRowCount() != 0) {
                for (int i = 0; i < jTable3.getRowCount(); i++) {
                    try {
                        Connection conn = ConnectorProvider.getCon();
                        Statement st = conn.createStatement();
                        st.executeUpdate("update product set quantity=quantity-" + Integer.parseInt(dtm.getValueAt(i, 2).toString()) + " where product_pk=" + Integer.parseInt(dtm.getValueAt(i, 0).toString()));

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                }
            }

            try {
                SimpleDateFormat myFormat = new SimpleDateFormat("dd-MM-yyyy");
                Calendar cal = Calendar.getInstance();
                Connection conn = ConnectorProvider.getCon();
                PreparedStatement ps = conn.prepareStatement("insert into orderdetails(orderId,customer_fk,orderDate,totalPaid) values(?,?,?,?)");
                ps.setString(1, orderId);
                ps.setInt(2, customerPk);
                ps.setString(3, myFormat.format(cal.getTime()));
                ps.setInt(4, finalTotalPrice);
                ps.executeUpdate();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

            //Creating document
            com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
            try {
                SimpleDateFormat myFormat = new SimpleDateFormat("dd-MM-yyyy");
                Calendar cal = Calendar.getInstance();
                PdfWriter.getInstance(doc, new FileOutputStream(InventoryUtils.billPath + "" + orderId + ".pdf"));
                doc.open();
                Paragraph projectName = new Paragraph("Inventory Management System");
                doc.add(projectName);

                Paragraph starLine = new Paragraph("**************************************************************************************************************************************************");
                doc.add(starLine);

                Paragraph details = new Paragraph("\tCustomer Name : "+ jTextField1.getText().toString()+ "\nContact Number : "+ jTextField2.getText().toString() + "\nEmail-id : "+ jTextField3.getText().toString() + "\nOrder ID : " + orderId + "\nDate : " + myFormat.format(cal.getTime()) + "\nTotal Paid : " + finalTotalPrice);
                doc.add(details);
                doc.add(starLine);

                PdfPTable tbl1 = new PdfPTable(5);
                PdfPCell nameCell = new PdfPCell(new Phrase("Name "));
                PdfPCell descriptionCell = new PdfPCell(new Phrase("Description "));
                PdfPCell priceCell = new PdfPCell(new Phrase("Price per Unit "));
                PdfPCell quantityCell = new PdfPCell(new Phrase("Quantity "));
                PdfPCell subtotalCell = new PdfPCell(new Phrase("Subtotal "));

                BaseColor bgColor = new BaseColor(255, 204, 51);
                nameCell.setBackgroundColor(bgColor);
                descriptionCell.setBackgroundColor(bgColor);
                priceCell.setBackgroundColor(bgColor);
                quantityCell.setBackgroundColor(bgColor);
                subtotalCell.setBackgroundColor(bgColor);

                tbl1.addCell(nameCell);
                tbl1.addCell(descriptionCell);
                tbl1.addCell(priceCell);
                tbl1.addCell(quantityCell);
                tbl1.addCell(subtotalCell);

                for (int i = 0; i < jTable3.getRowCount(); i++) {
                    tbl1.addCell(jTable3.getValueAt(i, 1).toString());
                    tbl1.addCell(jTable3.getValueAt(i, 4).toString());
                    tbl1.addCell(jTable3.getValueAt(i, 3).toString());
                    tbl1.addCell(jTable3.getValueAt(i, 2).toString());
                    tbl1.addCell(jTable3.getValueAt(i, 5).toString());

                }

                doc.add(tbl1);
                doc.add(starLine);
                Paragraph thanksMsg = new Paragraph("Thanks for shopping!");
                doc.add(thanksMsg);
                OpenPdf.OpenById(orderId);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

            doc.close();
            setVisible(false);
            new ManageOrder().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Please add some product to the cart or select Customer");
        }

    }//GEN-LAST:event_jButton6ActionPerformed

